def clean_stem(stem: str) -> str:
    stem = re.split(r"[三四]、(?:简答题|材料分析题)", stem)[0]
    return stem.replace("〈西米学府团队整理>", "").replace("《西来学府团队整理>", "").strip()


def find_points(stem: str) -> list[str]:
    for needles, points in SHORT_RULES:
        if all(needle in stem for needle in needles):
            return points
    for needles, points in SHORT_RULES:
        if any(needle in stem for needle in needles):
            return points
    return ["准确写出核心概念", "按理论要点分条说明", "结合题干情境进行解释", "语言规范、逻辑完整"]


def tf_answer(exam: str, number: int, stem: str) -> tuple[str, list[str]]:
    correct = (exam, number) in TRUE_ITEMS
    points = next((p for needles, p in REASON_RULES if all(n in stem for n in needles)), None)
    if points is None:
        points = next((p for needles, p in REASON_RULES if any(n in stem for n in needles)), None)
    if points is None:
        points = ["先界定题干中的核心概念", "指出题干表述成立或不成立的条件", "说明正确理论观点并作出结论"]
    judgment = "正确" if correct else "错误"
    return f"{judgment}。{'；'.join(points)}。", [f"明确判断：{judgment}", *points]


